    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int option=0;
        if(String.valueOf(jTextField1.getText()).isEmpty()){
        JOptionPane.showMessageDialog(null,"Please select an order","Warning",JOptionPane.WARNING_MESSAGE);
    } 
    else{   
        SimpleDateFormat fmt=new SimpleDateFormat("yyyy/MM/dd");
        
        option=JOptionPane.showConfirmDialog(this,"Do you want to add shipment date "+fmt.format(jDateChooser1.getDate())+" for "+String.valueOf(jTextField1.getText()),"Confirmation", JOptionPane.YES_OPTION);
     
    if(option==JOptionPane.YES_OPTION){
        CustomerOrderDbAccess dba=new CustomerOrderDbAccess("largescaleorder", "orderId");
        dba.updateField("shipmentDate",fmt.format(jDateChooser1.getDate()), String.valueOf(jTextField1.getText()));
        JOptionPane.showMessageDialog(null,"Successfully Added shipment Date ");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.removeRow(jTable1.getSelectedRow());
        jTextField1.setText("");
        jDateChooser1.setDate(null);
       
     }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

