    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(txtF650EmpID.getText().isEmpty()||cmb650Month.getSelectedItem()=="Select"||cmb651Year.getSelectedItem()=="Select"){
            JOptionPane.showMessageDialog(this,"Please enter the Employee ID and Select Month and Year!","Warning ",JOptionPane.WARNING_MESSAGE);
        }else{
        
        try {
            String month = hrdac.ReturnMonthNo(cmb650Month);
            hrdac.ReturnEmpDevision(txtF650EmpID, lbl656Devision);
            hrdac.ReturnEmpName(txtF650EmpID, lbl655Name);
            hrdac.ReturnLeaves(txtF650EmpID, month, cmb651Year, lbl650Leaves, lbl652PresentDays);
            hrdac.RetrieveAtt(txtF650EmpID, lbl650AvgDuration , month, cmb651Year);
            hrdac.LoadAttendance(tbl650Att, txtF650EmpID, month, cmb651Year);
        } catch (Exception e) {
        }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

