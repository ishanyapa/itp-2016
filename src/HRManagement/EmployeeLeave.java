    private void btnaddleaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddleaveActionPerformed
    String fromdate="";
    String todate="";
    
        try {
            java.util.Date dateFromDateChooser1 = jdate660From.getDate();
            fromdate = String.format("%1$tY-%1$tm-%1$td", dateFromDateChooser1);
            
            java.util.Date dateFromDateChooser2 = jdate661To.getDate();
            todate = String.format("%1$tY-%1$tm-%1$td", dateFromDateChooser2);
        } catch (Exception e) {
        }
        
        if(txt661EmpID.getText().isEmpty()||txtarea660Reason.getText().isEmpty() || String.valueOf(fromdate).isEmpty() || String.valueOf(todate).isEmpty()){
            JOptionPane.showMessageDialog(this,"One or more fields contains invalid data","Warning ",JOptionPane.WARNING_MESSAGE);
        }else{
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to proceed?");
                if(dialogResult == JOptionPane.YES_OPTION){
                    hrdac.AddLeave(txt667LveID, txt661EmpID, txtarea660Reason, fromdate, todate);
                    JOptionPane.showMessageDialog(this,"Record Succesfully added","Success! ",JOptionPane.INFORMATION_MESSAGE);
                    hrdac.LoadPendingLeave(jtbl660PendingRequestTable);
                    hrdac.cleanLeave(txt661EmpID, lbl664Name, txtarea660Reason, jdate660From, jdate661To);
                    hrdac.LeaveIDIncrement(txt667LveID);
                }
        }
    }//GEN-LAST:event_btnaddleaveActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to reject this leave request?");
        if(dialogResult == JOptionPane.YES_OPTION){
            hrdac.RejectLeave(lbl667lID);
            hrdac.LoadPendingLeave(jtbl660PendingRequestTable);
            hrdac.cleanApprvedRejectedLeave(jlbl660EmpID, lbl667lID, txtarea662Reason, txtfrom, txtto);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

