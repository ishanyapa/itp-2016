    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            DbConnect dbcon = new DbConnect();
            dbcon.setConnection();
            PreparedStatement pstmt;
            ResultSet rslt;
            
            pstmt = dbcon.con.prepareStatement("SELECT * FROM userView WHERE userName=? AND password=?");
            GenerateMd5 md5 = new GenerateMd5(String.valueOf(psswdUser.getPassword()));
            
            pstmt.setString(1,String.valueOf(txtUsername.getText()));
            pstmt.setString(2,md5.getMd5());
            
            rslt = pstmt.executeQuery();
            
            if(rslt.next()){
              
               SessionHandler.setUser(rslt.getNString("userName"));
               SessionHandler.setAccessLevel(rslt.getString("accessLevel"));
               SessionHandler.setOccupation(rslt.getString("jobName"));
               SessionHandler.setDivision(rslt.getString("division"));
               
               
            }
            else{
                JOptionPane.showMessageDialog(this,"Username or Password is invalid","Error ",JOptionPane.ERROR_MESSAGE);
            }
            
            if("Privileged User".equals(SessionHandler.getAccessLevel())){
                switch(SessionHandler.getDivision()){
                    case("Human Resource"):
                        HRManagement hrm = new HRManagement();
                        hrm.setVisible(true);
                        break;
                    case("Purchase"):
                        PurchaseManagement.PurchaseManagementMain pm = new PurchaseManagementMain();
                        pm.setVisible(true);
                        break;
                    case("Manufacturing"):    
                        ManufacturingManagement.ManufacturingMain mm = new ManufacturingMain();
                        mm.setVisible(true);
                        break;
                    case("Technical Services"):
                        Technical_Services.Technical_Services_main tsm = new Technical_Services_main();
                        tsm.setVisible(true);
                        break;
                    case("Customer Order Management"):
                        CustomerOrderManagement.CustomerOrderManagementMain com = new CustomerOrderManagementMain();
                        com.setVisible(true);
                        break;
                    case("Distribution Management"):
                        DistributionManagement dm = new DistributionManagement();
                        dm.setVisible(true);
                        break;
                    case("Warehouse"):
                        WarehouseMain wm = new WarehouseMain();
                        wm.setVisible(true);
                        break;
                }
            }
            else if("System Administrator".equals(SessionHandler.getAccessLevel())){
                ControlPanel cp = new ControlPanel();
                cp.setVisible(true);
            }
            
            this.dispose();
            dbcon.con.close();
            pstmt.close();
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnLoginActionPerformed

