    private void SendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendActionPerformed
        String host="smtp.gmail.com";
        // final String user = semail.getText();//Email Address
        // final String password = new String(pword.getPassword());// password
        final String user = "sales.kelanicables@gmail.com";
        final String password = "$root123";

        if(!user.equals("") && !password.equals(""))
        {
            String SMTP_PORT = "465";
            String SSL_FACTORY = "javax.net.ssl.SSLSocketFactory";

            //String to="bitf11m036@pucit.edu.pk";//change accordingly
            String to=remail.getText();

            //Get the session object
            jProgressBar1.setValue(10);
            Properties props = new Properties();
            props.put("mail.smtp.starttls.enable", "true");
            props.put("mail.smtp.host",host);
            props.put("mail.smtp.auth", "true");
            props.put("mail.debug", "true");
            props.put("mail.smtp.port", SMTP_PORT);
            props.put("mail.smtp.socketFactory.port", SMTP_PORT);
            props.put("mail.smtp.socketFactory.class", SSL_FACTORY);
            props.put("mail.smtp.socketFactory.fallback", "false");

            Session session = Session.getDefaultInstance(props,
                new javax.mail.Authenticator()
                {

                    protected PasswordAuthentication getPasswordAuthentication()
                    {
                        return new PasswordAuthentication(user,password);
                    }
                });

                //Compose the message
                try
                {
                    MimeMessage message = new MimeMessage(session);
                    // creates message part
                    MimeBodyPart messageBodyPart = new MimeBodyPart();
                    messageBodyPart.setContent(message, "text/html");
                    Multipart multipart = new MimeMultipart();//new additions

                    message.setFrom(new InternetAddress(user));
                    message.addRecipient(Message.RecipientType.TO,new InternetAddress(to));
                    //message.setSubject("Subject");
                    message.setSubject(subject.getText());
                    message.setText(content.getText());
                    //send the message
                    //  Transport.send(message);

                    if(attachmentPath.equals(" "))
                    {
                        Transport.send(message);  //send only message
                    }

                    else
                    {
                        //send message with atttchment
                        jProgressBar1.setValue(40);
                        File att = new File(new File(attachmentPath.substring(0,attachmentPath.lastIndexOf("\\")+1).replace("\\","\\\\")), attachmentName);

                            messageBodyPart.attachFile(att);
                            DataSource source = new FileDataSource(att);
                            messageBodyPart.setDataHandler(new DataHandler(source));
                            messageBodyPart.setFileName(attachmentName);
                            multipart.addBodyPart(messageBodyPart);

                            message.setContent(multipart);
                            Transport.send(message);

                        }

                        DisplayMessage.display("Message Sent Successfully!");
                        //JOptionPane.showMessageDialog(null,"message sent successfully...");
                    }

                    catch (MessagingException e)
                    {
                        e.printStackTrace();
                    }
                    catch (IOException ex)
                    {
                        Logger.getLogger(NotifyCustomer.class.getName()).log(Level.SEVERE, null, ex);

                    }
                }

                else
                {
                    DisplayMessage.display("Invalid Field Entry");
                    //JOptionPane.showMessageDialog(null,"Dear User! Please Enter Email or Password");
                }
    }//GEN-LAST:event_SendActionPerformed

