    private void btn246SendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn246SendActionPerformed
        if(tbl245Materials.getRowCount()>0){
            int option = JOptionPane.showConfirmDialog(this,"Do you wish to proceed ?");
            if(option==JOptionPane.YES_OPTION){
                wdba.SendPurchaseRequest(tbl245Materials);
                lbl247Items.setText("0 Items Added");
                JOptionPane.showMessageDialog(this,"Request sent successfully");
                CheckReorderLevel ckrl = new CheckReorderLevel();
                SetPanel sp = new SetPanel();
                sp.SetPanel(ckrl);
            }
            
        }
        else
            JOptionPane.showMessageDialog(this,"Please add raw materials to the request","Warning ",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btn246SendActionPerformed

