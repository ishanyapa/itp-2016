    private void btn244AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn244AddActionPerformed
       if(!txtf242PID.getText().equals("")){
           if(Integer.parseInt(String.valueOf(spnnr243Qty.getValue()))>0){
               Object in[] = {txtf242PID.getText(),spnnr243Qty.getValue()};
               DefaultTableModel model = (DefaultTableModel) tbl245Materials.getModel();
               model.addRow(in);
               lbl247Items.setText(String.valueOf(Integer.parseInt(String.valueOf(lbl247Items.getText().charAt(0)))+1)+" Items Added");
               txtf242PID.setText("");
               spnnr243Qty.setValue(0);
               cmb242RMID.setSelectedIndex(-1);
           }
       }
       else{
           JOptionPane.showMessageDialog(this,"One or more fields contain invalid data","Warning ",JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_btn244AddActionPerformed

