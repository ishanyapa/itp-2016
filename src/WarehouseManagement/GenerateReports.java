    private void btn291GraphActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn291GraphActionPerformed
       if(date290From.getDate()!=null && date290To.getDate()!=null){
           jpnl293Graph.removeAll();
           jpnl293Graph.repaint();
           jpnl293Graph.revalidate();
           btn294Save.setEnabled(true);
           DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
           String from = dateFormat.format(date290From.getDate());
           String to = dateFormat.format(date290To.getDate());
           String query = "select itemName, sum(quantity) as quantity from viewissuedmaterials where requestDate between '"+from+"' AND '"+to+"' group by rawMaterialId";
           JPanel pnl;

           Chart inputMaterials = new Chart();

           inputMaterials.createPieDataset(query,"itemName","quantity");
           pnl = inputMaterials.createPanel(chrt=inputMaterials.createPieChart("Issued Raw Materials from "+from+" to "+to),700,270);

           SetPanel sp = new SetPanel();
           sp.SetPanel(jpnl293Graph, pnl);
       }
       else{
           JOptionPane.showMessageDialog(this,"One or more fields contains invalid data","Warning ",JOptionPane.WARNING_MESSAGE);
       }
       
       
    }//GEN-LAST:event_btn291GraphActionPerformed

    private void btn294SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn294SaveActionPerformed
        try {
            FileNameExtensionFilter ff = new FileNameExtensionFilter(".jpeg files","jpeg");
            JFileChooser jfc = new JFileChooser();

            jfc.setFileFilter(ff);
            jfc.showSaveDialog(this);
            File chartFile = jfc.getSelectedFile();
            String path = chartFile.getAbsolutePath();
            path = path+".jpeg";
            chartFile = new File(path);
            Chart saveChart = new Chart();
            saveChart.SaveChart(chartFile, chrt);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn294SaveActionPerformed

