    private void btn291Graph1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn291Graph1ActionPerformed
        if(date290From1.getDate()!=null && date290To1.getDate()!=null && cmb295OType.getSelectedIndex()!=-1){
            jpnl293Graph1.removeAll();
            jpnl293Graph1.repaint();
            jpnl293Graph1.revalidate();

            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String from = dateFormat.format(date290From1.getDate());
            String to = dateFormat.format(date290To1.getDate());
            JPanel pnl;
            String query="";
            
            if(cmb295OType.getSelectedIndex()==0)
                query = "select productId,name, quantity from viewlargeissuedproducts where invoiceDate between '"+from+"' AND '"+to+"' group by productId";
            else if(cmb295OType.getSelectedIndex()==1)
                query = "select productId,name, quantity from viewsmallissuedproducts where invoiceDate between '"+from+"' AND '"+to+"' group by productId";
            
            Chart inputMaterials = new Chart();
            inputMaterials.createPieDataset(query,"name","quantity");
            pnl = inputMaterials.createPanel(chrt=inputMaterials.createPieChart("Issued Finished Products from "+from+" to "+to),700,250);
            SetPanel sp = new SetPanel();
            sp.SetPanel(jpnl293Graph1, pnl);
            btn294Save1.setEnabled(true);
        }
        else
            JOptionPane.showMessageDialog(this,"One or more fields contains invalid data","Warning ",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btn291Graph1ActionPerformed

