    private void SetTableHeaders()
    {
        TableColumnModel tcm = tbl260Request.getColumnModel();
        tcm.getColumn(0).setHeaderValue("Request ID");
        tcm.getColumn(1).setHeaderValue("Raw Material ID");
        tcm.getColumn(2).setHeaderValue("Item Name");
        tcm.getColumn(3).setHeaderValue("Quantity");
        tcm.getColumn(4).setHeaderValue("Status");
        tcm.getColumn(5).setHeaderValue("Request Date");
    }

