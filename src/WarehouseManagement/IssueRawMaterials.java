    private void btn261ViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn261ViewActionPerformed
       
        lblrecords.setVisible(true);
        wdba.SearchByKeyWord(tbl260Request,"viewRequest", "Unassigned","status","");
        lblrecords.setText(String.valueOf(tbl260Request.getRowCount())+" Records found");
        SetTableHeaders();
        if(tbl260Request.getRowCount()>0){
            btn263Load.setEnabled(true);
            btn264Assign.setEnabled(true);
        }
    }//GEN-LAST:event_btn261ViewActionPerformed

    private void btn263LoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn263LoadActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl262Materials.getModel();
        System.out.println(model.getRowCount());
        if(model.getRowCount()>0){
            for(int j=model.getRowCount()-1;j>=0;j--){
               model.removeRow(j);
            }
        }
        
        for(int i=0;i<tbl260Request.getRowCount();i++){
            
           Object obj[] ={tbl260Request.getValueAt(i, 0),tbl260Request.getValueAt(i, 1),tbl260Request.getValueAt(i, 3)};
           model.addRow(obj);
        }
    }//GEN-LAST:event_btn263LoadActionPerformed

    private void btn264AssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn264AssignActionPerformed
         
         int option=JOptionPane.showConfirmDialog(this,"Do you wish to proceed ?");
         if(option==JOptionPane.YES_OPTION){
            wdba.IssueRawMaterials(tbl262Materials);
            DefaultTableModel model = (DefaultTableModel) tbl262Materials.getModel();
            if(tbl262Materials.getRowCount()>0){
                for(int j=tbl262Materials.getRowCount()-1;j>=0;j--){
                    model.removeRow(j);
                    System.out.println(j);
                }
            }
         }
         
    }//GEN-LAST:event_btn264AssignActionPerformed

